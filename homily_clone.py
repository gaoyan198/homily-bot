"""
Homily Chart clone — "Red & White Circle" trend-regime engine.
============================================================

Homily Chart (Homilystone, est. 1998) is a SG/MY/CN technical-analysis platform.
Its signature proprietary indicator is the Red & White Circle (红白圈):
    RED circle  -> bullish regime -> HOLD the stock
    WHITE circle-> bearish regime -> HOLD cash / cut
The exact formula is undisclosed. This is a FAITHFUL APPROXIMATION of its
documented behaviour: a smoothed weekly trend-following regime classifier built
from a moving-average ribbon + MACD momentum + long-trend slope.

Pure stdlib (no pandas/numpy) so it runs anywhere. Weekly closes are embedded
below (pulled live from Interactive Brokers, 1y weekly bars, as of 2026-06-27).

A reusable yfinance-backed fetcher is included at the bottom for future runs.
"""

from dataclasses import dataclass

# ----------------------------------------------------------------------------
# Indicator math (stdlib only)
# ----------------------------------------------------------------------------

def ema(series, span):
    k = 2 / (span + 1)
    out = []
    e = series[0]
    for x in series:
        e = x * k + e * (1 - k)
        out.append(e)
    return out

def sma(series, n):
    out = []
    for i in range(len(series)):
        if i + 1 < n:
            out.append(None)
        else:
            out.append(sum(series[i + 1 - n:i + 1]) / n)
    return out

def macd(series, fast=12, slow=26, sig=9):
    ef, es = ema(series, fast), ema(series, slow)
    line = [a - b for a, b in zip(ef, es)]
    signal = ema(line, sig)
    hist = [a - b for a, b in zip(line, signal)]
    return line, signal, hist


@dataclass
class Signal:
    ticker: str
    circle: str        # RED / WHITE / AMBER
    score: int         # 0..4
    price: float
    pct_vs_ma30: float # % above/below 30-week trend line
    weeks_in_regime: int
    detail: str


def homily_circle(ticker, closes):
    """4-factor weekly trend regime -> Red/White circle."""
    ema10 = ema(closes, 10)
    ema30 = ema(closes, 30)
    sma30 = sma(closes, 30)
    line, _, hist = macd(closes)

    def score_at(i):
        s = 0
        if closes[i] > ema30[i]:                       s += 1   # price above long trend
        if ema10[i] > ema30[i]:                        s += 1   # ribbon bullish (fast>slow)
        if hist[i] > 0 and line[i] > 0:                s += 1   # MACD momentum up AND positive
                                                                # (line>0 guard: a decelerating
                                                                # downtrend prints hist>0 too)
        if sma30[i] is not None and i >= 4 and sma30[i - 4] is not None \
                and sma30[i] > sma30[i - 4]:           s += 1   # long trend rising
        return s

    def circle_of(s):
        return "RED" if s >= 3 else ("WHITE" if s <= 1 else "AMBER")

    scores  = [score_at(i) for i in range(len(closes))]
    circles = [circle_of(s) for s in scores]

    # how many consecutive weeks the current circle colour has held
    cur = circles[-1]
    weeks = 1
    for c in reversed(circles[:-1]):
        if c == cur:
            weeks += 1
        else:
            break

    price = closes[-1]
    pct = (price / ema30[-1] - 1) * 100
    s = scores[-1]
    flags = []
    flags.append("px>MA30" if closes[-1] > ema30[-1] else "px<MA30")
    flags.append("EMA10>30" if ema10[-1] > ema30[-1] else "EMA10<30")
    flags.append("MACDup" if (hist[-1] > 0 and line[-1] > 0) else "MACDdn")
    slope_up = sma30[-1] is not None and sma30[-5] is not None and sma30[-1] > sma30[-5]
    flags.append("trendUp" if slope_up else "trendDn")
    return Signal(ticker, circle_of(s), s, price, pct, weeks, " ".join(flags))


